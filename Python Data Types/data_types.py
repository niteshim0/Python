# Numeric Types
integer_num = 5
float_num = 3.14

# Sequence Types
string_val = "Hello, Python!"
list_val = [1, 2, 3, 4, 5]
tuple_val = ('a', 'b', 'c')

# Mapping Types
dictionary_val = {'name': 'John', 'age': 30}

# Set Types
set_val = {1, 2, 3, 4, 5}
frozen_set_val = frozenset({'apple', 'banana', 'orange'})

# Boolean Type
bool_val = True

# Binary Types
bytes_val = b'binary data'
bytearray_val = bytearray(b'byte array')

# None Type
none_val = None

# Print the data types and their values
print("Numeric Types:")
print("Integer:", integer_num, "-", type(integer_num))
print("Float:", float_num, "-", type(float_num))

print("\nSequence Types:")
print("String:", string_val, "-", type(string_val))
print("List:", list_val, "-", type(list_val))
print("Tuple:", tuple_val, "-", type(tuple_val))

print("\nMapping Types:")
print("Dictionary:", dictionary_val, "-", type(dictionary_val))

print("\nSet Types:")
print("Set:", set_val, "-", type(set_val))
print("Frozen Set:", frozen_set_val, "-", type(frozen_set_val))

print("\nBoolean Type:")
print("Boolean:", bool_val, "-", type(bool_val))

print("\nBinary Types:")
print("Bytes:", bytes_val, "-", type(bytes_val))
print("Bytearray:", bytearray_val, "-", type(bytearray_val))

print("\nNone Type:")
print("None:", none_val, "-", type(none_val))
