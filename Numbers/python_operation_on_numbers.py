# Arithmetic Operations on Numbers in Python
# Addition
result = 5 + 3
print("Addition:", result)

# Subtraction
result = 5 - 3
print("Subtraction:", result)

# Multiplication
result = 5 * 3
print("Multiplication:", result)

# Division
result = 5 / 3
print("Division:", result)

# Integer Division
result = 5 // 3
print("Integer Division:", result)

# Modulo (Remainder)
result = 5 % 3
print("Modulo:", result)

# Exponentiation
result = 5 ** 3
print("Exponentiation:", result)

# Bitwise Operations on Numbers in Python

# Bitwise AND
result = 5 & 3
print("Bitwise AND:", result)

# Bitwise OR
result = 5 | 3
print("Bitwise OR:", result)

# Bitwise XOR
result = 5 ^ 3
print("Bitwise XOR:", result)

# Bitwise NOT
result = ~5
print("Bitwise NOT:", result)

# Left Shift
result = 5 << 1
print("Left Shift:", result)

# Right Shift
result = 5 >> 1
print("Right Shift:", result)

# Comparison Operations on Numbers in Python

#Equal to
result = 5 == 3
print("Equal to:", result)

# Not equal to
result = 5 != 3
print("Not equal to:", result)

# Greater than
result = 5 > 3
print("Greater than:", result)

# Less than
result = 5 < 3
print("Less than:", result)

# Greater than or equal to
result = 5 >= 3
print("Greater than or equal to:", result)

# Less than or equal to
result = 5 <= 3
print("Less than or equal to:", result)